# And Operators - If Both Conditions Are True, Then It Will Be True, Otherwise False.
print(True and True)
print(True and False)
print(False and False)
print(False and True)

# Or Operators - If At Least One Condition Are True, Then It Will Be True, Otherwise Flase.
print(True or True)
print(True or False)
print(False or True)
print(False or False)

# Not Operator
name = 'dog'
print(not True)
print(not False)
print(name == 'dog')

# ---> Not Operation
Boolean = ( not 10 == 10)
print(Boolean)

# ---> Or Operation --> One Must Be True
Boolean2 = (10 < 20) or (20 < 10)
print(Boolean2)

# ---> And Operation --> Both Must Be True
Boolean3 = (10 < 20) and (30 > 20)
print(Boolean3)