# Comparison Operator

print(20 == 20) # Equal To
print(20 != 21) # Not Equal To
print(20 > 21) # Greater Than
print(20 < 21) #Less Than
print(20 <= 19) # Less Than Or Equal To
print(20 >= 19) # Greater Than Or Equal To
print(20 == 20.0)

